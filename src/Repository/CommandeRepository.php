<?php

namespace App\Repository;

use App\Entity\Commande;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Commande>
 *
 * @method Commande|null find($id, $lockMode = null, $lockVersion = null)
 * @method Commande|null findOneBy(array $criteria, array $orderBy = null)
 * @method Commande[]    findAll()
 * @method Commande[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class CommandeRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Commande::class);
    }

    /**
     * Add a new Order to the DB
     * @param Commande $entity
     * @param boolean $flush
     * @return void
     */
    public function add(Commande $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    /**
     * WARNING : Must not be used without manager auth
     * Must invalid order than delete
     * --- Remove an order from DB -----
     * @param Commande $entity
     * @param boolean $flush
     * @return void
     */
    public function remove(Commande $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    /**
     * Find All Orders with the given state
     * @param string $state
     * @return Commande[]
     */
    public function findAllByState(string $state):array{
        return $this->createQueryBuilder('c')
                   ->andWhere('c.statut = :val')
                   ->setParameter('val', $state)
                   ->orderBy('c.id', 'ASC')
                   ->getQuery()
                   ->getResult()
               ;
    }

        /**
         * Find one commande by the given id
         *
         * @param [type] $value
         * @return Commande|null
         */
        public function findOneCommandeById($value): ?Commande
    {
        return $this->createQueryBuilder('c')
            ->andWhere('c.id = :val')
            ->setParameter('val', $value)
            ->getQuery()
            ->getOneOrNullResult()
        ;
    }

